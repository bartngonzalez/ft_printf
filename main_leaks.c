#include "ft_printf.h"

int		main(void)
{
	char *pstr = "42";
	ft_printf("(c),   42 = [%c]\n", 42);
	ft_printf("(s),     42 = [%s]\n", "42");
	ft_printf("(-d),    2147483648 = [%-d]\n", (unsigned int)2147483648);
	ft_printf("(-10.5i),    -4242 = [%-10.5i]\n", -4242);
	ft_printf("(#.o),           -42 = [%#.o]\n", -42);
	ft_printf("(-u),                         42 = [%-u]\n", 42);
	ft_printf("(-#26.20x),                           -42 = [%-#26.20x]\n", -42);
	ft_printf("(#20x),                              -42 = [%#20x]\n", -42);
	ft_printf("(-#f),                           -42.00 = [%-#f]\n", -42.00);
	while (1)
	{
		;
	}
	return (0);
}
